internal class Huffman {
    private class Node {
        var parent: Node? = null
        val child: Array<Node?> = arrayOfNulls<Node>(2)
        var next: Node? = null
        var prev: Node? = null
        var value: Int = 0
        var probability: Int = 0

        fun treeSwap(with: Node) {
            var temp: Node?

            if (parent === with.parent) {
                temp = parent!!.child[0]
                parent!!.child[0] = parent!!.child[1]
                parent!!.child[1] = temp
            } else {
                if (with.parent!!.child[0] === with) with.parent!!.child[0] = this
                else with.parent!!.child[1] = this
                if (this.parent!!.child[0] === this) this.parent!!.child[0] = with
                else this.parent!!.child[1] = with
            }

            temp = parent
            parent = with.parent
            with.parent = temp
        }

        fun insertAfter(where: Node) {
            prev = where
            next = where.next
            where.next = this
            next!!.prev = this
        }

        fun listSwap(with: Node) {
            if (next === with) {
                next = with.next
                with.next = this
                with.prev = prev
                prev = with

                with.prev!!.next = with
                next!!.prev = this
            } else if (prev === with) {
                prev = with.prev
                with.prev = this
                with.next = next
                next = with

                with.next!!.prev = with
                prev!!.next = this
            } else {
                var temp = prev!!
                prev = with.prev
                with.prev = temp

                temp = next!!
                next = with.next
                with.next = temp

                prev!!.next = this
                next!!.prev = this

                with.prev!!.next = with
                with.next!!.prev = with
            }
        }

        fun newList() {
            next = this
            prev = next
        }

        fun removeFromList(): Node? {
            if (this === next) return null

            //prev!!.next = next
            //next!!.prev = prev

            return next
        }

        fun joinList(list: Node) {
            val tail = prev!!

            prev = list.prev
            prev!!.next = this

            list.prev = tail
            tail.next = list
        }
    }

    private var nodes: Node? = null
    private val sorted2 = TreeMap<Int?, Node?>()

    private var root: Node? = null

    private var bitBuffer = 0
    private var bitNumber: Byte = 0
    private var source: ByteBuffer? = null

    private fun setSource(source: ByteBuffer) {
        this.source = source
        bitBuffer = 0
        bitNumber = 0
    }

    private fun getBits(bits: Int): Int {
        while (bitNumber < bits) {
            bitBuffer = bitBuffer or ((source!!.get().toInt() and 0xFF) shl bitNumber.toInt())
            bitNumber = (bitNumber + 8).toByte()
        }

        val result = bitBuffer and ((1 shl bits) - 1)
        bitBuffer = bitBuffer ushr bits
        bitNumber = (bitNumber - bits).toByte()

        return result
    }

    private val node: Node
        get() {
            val node: Node?
            if (nodes == null) node = Node()
            else {
                node = nodes
                nodes = nodes!!.removeFromList()
            }
            return node!!
        }

    private fun destroyTree(root: Node) {
        if (nodes == null) nodes = root
        else nodes!!.joinList(root)
        this.root = null
        sorted2.clear()
    }

    private fun insertNode(node: Node) {
        val test2 = sorted2.ceilingEntry(node.probability)
        val current: Node


        if (test2 != null) {
            current = test2.value!!
            node.insertAfter(current)
        } else {
            if (root != null) {
                node.insertAfter(root!!.prev!!)
            } else {
                node.newList()
            }
            root = node
        }

        sorted2.put(node.probability, node)
    }

    private fun addValueToTree(value: Int): Node {
        // create leaf node
        val node = this.node
        node.value = value
        node.probability = 0
        node.child[0] = null
        node.child[1] = null

        insertNode(node)

        // create branch node
        val node2 = this.node
        val child1 = root!!.prev!!
        val child2 = child1.prev!!

        node2.value = -1
        node2.probability = child1.probability + child2.probability
        node2.child[0] = child1
        node2.child[1] = child2
        node2.parent = child2.parent

        node2.insertAfter(child2.prev!!)

        // insert into tree
        if (node2.parent!!.child[0] === child2) node2.parent!!.child[0] = node2
        else node2.parent!!.child[1] = node2

        child1.parent = node2
        child2.parent = node2

        return node
    }

    private fun incrementProbability(node: Node?) {
        var node = node
        while (node != null) {
            // possible optimization here. Is all this really nescescary to enforce order?
            if (sorted2.get(node.probability) === node) {
                if (node.probability == node.prev!!.probability) sorted2.put(node.probability, node.prev)
                else sorted2.remove(node.probability)
            }
            node.probability += 1

            val test2 = sorted2.ceilingEntry(node.probability)
            val where: Node?
            if (test2 != null) where = test2.value!!.next
            else where = root

            if (where !== node) {
                node.listSwap(where!!)
                node.treeSwap(where)

                if (where.probability != where.next!!.probability) {
                    sorted2.put(where.probability, where)
                }
            }
            sorted2.put(node.probability, node)

            node = node.parent
        }
    }

    private fun buildTree(tree: Byte) {
        val probabilities: ByteArray = PROBABILITY_TABLES[tree.toInt()]

        // destroy any existing tree
        if (root != null) destroyTree(root!!)

        // generate leaves
        for (i in 0..0x101) {
            val prob = probabilities[i].toInt() and 0xFF

            if (prob == 0) continue

            val node = this.node
            node.value = i
            node.probability = prob
            node.child[0] = null
            node.child[1] = null

            insertNode(node)
        }

        // generate tree
        var current = root!!.prev!!
        while (current !== root) {
            val node = this.node
            val child1: Node? = current
            current = current.prev!!
            val child2 = current

            child1!!.parent = node
            child2.parent = node

            node.value = -1
            node.probability = child1.probability + child2.probability
            node.child[0] = child1
            node.child[1] = child2
            insertNode(node)

            current = current.prev!!
        }

        root!!.parent = null
    }

    fun Decompress(`in`: ByteBuffer, out: ByteBuffer) {
        setSource(`in`)
        val type = getBits(8).toByte()
        buildTree(type)

        val adjustProbability = type.toInt() == 0

        while (true) {
            var current = root
            while (current!!.value == -1) current = current.child[getBits(1)]

            if (current.value == 0x101) {
                val value = getBits(8)
                current = addValueToTree(value)
                incrementProbability(current)
                if (!adjustProbability) incrementProbability(current)
            } else if (current.value == 0x100) {
                break
            }

            out.put(current.value.toByte())

            if (adjustProbability) {
                incrementProbability(current)
            }
        }
    }

